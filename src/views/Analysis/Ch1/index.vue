      <p>In the years between 1967 and 2013, major Australian bushfires have resulted in over 8000 injuries and 433 fatalities, close to 50 per cent of all deaths from major Australian natural disasters in the period (excluding heatwaves). Over this same period, bushfires cost approximately A$4.7 billion (2013 Australian dollars, including deaths and injuries but excluding most indirect losses).</p>
      <p>Bushfire activity varies across Australia with the changes in the seasonal weather patterns. Wind, temperature, humidity and rainfall are weather elements that affect the behaviours of bushfires.</p>
