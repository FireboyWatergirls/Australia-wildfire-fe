      <h2>What is a bushfire?</h2>
      <p>Bushfires and grassfires are common throughout Australia. Grassfires are fast moving, passing in five to ten seconds and smouldering for minutes. They have a low to medium intensity and primarily damage crops, livestock and farming infrastructure, such as fences. Bushfires are generally slower moving, but have a higher heat output. This means they pass in two to five minutes, but they can smoulder for days. Fire in the crown of the tree canopy can move rapidly.</p>
      <p>Bushfires are an intrinsic part of Australia's environment. Natural ecosystems have evolved with fire, and the landscape, along with its biological diversity, has been shaped by both historic and recent fires. Many of Australia's native plants are fire prone and very combustible, while numerous species depend on fire to regenerate. Indigenous Australians have long used fire as a land management tool and it continues to be used to clear land for agricultural purposes and to protect properties from intense, uncontrolled fires.</p>
      <p>Historically, bushfires have caused loss of life and significant damage to property. While naturally occurring bushfires cannot be averted, their consequences can be minimised by implementing mitigation strategies and reducing the potential impact to areas which are most vulnerable.</p>
