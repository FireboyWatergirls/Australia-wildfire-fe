      <h2>Where do bushfires occur?</h2>
      <p>The Australian climate is generally hot, dry and prone to drought. At any time of the year, some parts of Australia are prone to bushfires. The widely varied fire seasons are reflected in the continent's different weather patterns. For most of southern Australia, the danger period is summer and autumn. For New South Wales and southern Queensland, the peak risk usually occurs in spring and early summer. The Northern Territory experiences most of its fires in winter and spring.</p>
      <p>Grassland fires frequently occur after good periods of rainfall which result in abundant growth that dries out in hot weather. Bushfires tend to occur when light and heavy fuel loads in Eucalypt forests have dried out, usually following periods of low rainfall.</p>
      <p>The potential for extreme fire weather varies greatly throughout Australia, both in frequency and severity. When potential extreme fire weather is experienced close to populated areas, significant loss is possible. In terms of the total area burnt, the largest fires are in the Northern Territory and northern areas of Western Australia and Queensland. Most loss of life and economic damage occurs around the fringes of cities where homes are commonly in close proximity to flammable vegetation.</p>
