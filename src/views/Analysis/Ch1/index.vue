      <h2>What factors create a favourable environment for bushfires to occur?</h2>
      <p>The basic factors which determine whether a bushfire will occur include the presence of fuel, oxygen and an ignition source. The fire intensity and speed at which a bushfire spreads will depend on ambient temperature, fuel load, fuel moisture, wind speed and slope angle.</p>
      <h3>Fuel load</h3>
      <p>Fuel load describes the amount of fallen bark, leaf litter and small branches accumulating in the landscape. Generally speaking, the greater the fuel load, the hotter and more intense the fire. Fuel which is concentrated but loosely compacted will burn faster than heavily compacted or scattered fuel sources. Smaller pieces of fuel such as twigs, leaf litter and branches burn quickly, particularly when they are dry and loosely arranged and will burn quickly in the fire front. Larger fuels, such as tree trunks often burn later after the fire front has passed. The natural oil within eucalypt trees promotes the combustion of fuel.</p>
      <h3>Fuel moisture</h3>
      <p>Dry fuel will burn quickly, but damp or wet fuel may not burn at all. As a consequence, the time since rainfall and the amount of rain received is an important consideration in assessing bushfire danger. Often a measure of the drought factor, or moisture deficit, will be used as an indicator of extreme bushfire weather conditions.</p>
      <h3>Wind speed</h3>
      <p>Wind acts to drive a fire by blowing the flames into fresh fuel, bringing it to ignition point and providing a continuous supply of oxygen. Wind also promotes the rapid spread of fire by spotting, which is the ignition of new fires by burning embers lofted into the air by wind. Spotting can occur up to 30km downwind from the fire front.</p>
      <p>There is a threshold wind speed of around 12 to 15km/h which makes a significant difference in the behaviour of bushfires in the open. When wind speeds are below this threshold, fires with heavy fuel loads burn slowly. However, even a slight increase in wind speed above this threshold results in a significant increase in fire behaviour and advancement. The width of a fire front also has an influence on the rate of spread and a wind shift can immediately widen the forward edge of a fire.</p>
      <h3>Ambient temperature</h3>
      <p>The higher the temperature the more likely it is that a fire will start or continue to burn. This is because the fuel is closer to its ignition point at high temperatures and pre-heated fuel loads burn faster.</p>
      <h3>Relative humidity</h3>
      <p>Dry air promotes a greater intensity fire than moist air. Plants become more flammable at a low humidity because they release their moisture more easily.</p>
      <h3>Slope angle</h3>
      <p>Fires pre-heat their fuel source through radiation and convection. As a result, fires accelerate when travelling uphill and decelerate travelling downhill. The steepness of the slope plays an important role in the rate of fire spread. The speed of a fire front advancing will double with every 10 degree increase in slope, so that on a 20 degree slope, its speed of advance is four times greater than on flat ground.</p>
      <h3>Ignition Source</h3>
      <p>Bushfires can originate from both human activity and natural causes with lightning the predominant natural source, accounting for about half of all ignitions in Australia. Fires of human origin currently account for the remainder and are classified as accidental or deliberate. Fires lit deliberately can be the result of arson or might be designed to achieve a beneficial outcome but conditions have changed, resulting in uncontrollable spread.</p>
      <p>Unfortunately deliberate and accidentally lit fires are more prevalent near populated areas and have a disproportionately higher risk of infrastructure impact. Arsonists place people and property at serious and unnecessary risk, particularly when igniting fires on extreme fire weather days.</p>
